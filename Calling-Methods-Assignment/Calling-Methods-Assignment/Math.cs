using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calling_Methods_Assignment
{
    class Math
    {
        public static int Add(int num)
        {
           int sum = num + 5;
           return sum;
        }

        public static int Subtract(int num)
        {
            int sum = num - 5;
            return sum;
        }

        public static int Multiply(int num)
        {
            int sum = num * 5;
            return sum;
        }
    }
}

    
